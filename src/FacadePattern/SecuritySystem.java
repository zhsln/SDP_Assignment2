package FacadePattern;

public class SecuritySystem {
    public void turnOnCameras() {
        System.out.println("Security System is turned ON.");
    }
    public void turnOffCameras() {
        System.out.println("Security System is turned OFF.");
    }
    public void turnOnAlarm() {
        System.out.println("Security System is alarmed.");
    }

    public void turnOffAlarm() {
        System.out.println("Alarm system is turned OFF.");
    }
}
